function nndataimport(cmd,arg1,arg2,arg3)
%NNDATAIMPORT Data Import GUI for the Neural Network Controller Toolbox.
%
%  Synopsis
%
%    nndataimport(cmd,arg1,arg2,arg3)
%
%  Warning!!
%
